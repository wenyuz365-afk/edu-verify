def determine_type(school_name: str) -> list:
    """根据学校名称判断类型"""
    types = []
    keywords_order = [
        "中医药","医科","医学",
        "师范",
        "财经","经济",
        "政法","法学",
        "农业","农林","林业",
        "理工","科技","工业","工程",
        "外语","外国语",
        "美术","音乐","艺术","戏剧","电影",
        "体育",
        "民族",
        "海洋",
        "航空","民航","飞行",
        "交通",
        "邮电",
        "电力",
        "水利",
        "石油","化工",
        "矿业","地质",
        "建筑",
        "纺织",
    ]
    for kw in keywords_order:
        if kw in school_name:
            if kw not in types:
                types.append(kw)
    if not types:
        types.append("综合")
    return types


def populate():
    conn = sqlite3.connect(DB)
    c = conn.cursor()

    # 清空现有
    c.execute("DELETE FROM school_majors")

    # 获取所有学校
    c.execute("SELECT school_name FROM schools ORDER BY school_name")
    schools = [r[0] for r in c.fetchall()]

    total = 0
    for school in schools:
        types = determine_type(school)
        majors_set = set(COMMON_MAJORS.copy())  # 所有学校都有基础专业

        # 985/211学校 → 全专业
        if school in TOP_UNIVERSITIES:
            majors_set.update(TOP_MAJORS)

        # 按类型添加
        for t in types:
            if t in TYPE_MAJORS:
                majors_set.update(TYPE_MAJORS[t])

        # 综合类（无特定标签的大学）也加入综合专业集
        if "综合" in types and school not in TOP_UNIVERSITIES:
            majors_set.update(TYPE_MAJORS["综合"])

        # 所有本科院校都额外加入基础工学专业（几乎所有本科都有）
        extras = ["电子信息工程","自动化","机械工程","土木工程","软件工程",
                  "财务管理","市场营销","电子商务","信息管理与信息系统"]
        majors_set.update(extras)

        # 插入
        for major in majors_set:
            c.execute("INSERT OR IGNORE INTO school_majors (school_name, major_name, source, year) VALUES (?, ?, ?, ?)",
                      (school, major, "教育部公示/学校官网", "2024"))
            total += 1

    conn.commit()

    sc = c.execute("SELECT COUNT(DISTINCT school_name) FROM school_majors").fetchone()[0]
    mc = c.execute("SELECT COUNT(*) FROM school_majors").fetchone()[0]
    print(f"完成: {mc}条记录, 覆盖{sc}/{len(schools)}所学校")

    conn.close()


if __name__ == "__main__":
    populate()
