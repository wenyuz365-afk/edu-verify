def rebuild():
    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()

    # 删除旧表
    c.execute("DROP TABLE IF EXISTS schools")
    c.execute("DROP TABLE IF EXISTS mock_chsi")
    c.execute("DROP TABLE IF EXISTS verify_records")

    # 重建
    c.execute("""CREATE TABLE schools (
        school_code TEXT PRIMARY KEY, school_name TEXT, province TEXT, education_level TEXT)""")
    c.execute("""CREATE TABLE mock_chsi (
        id INTEGER PRIMARY KEY AUTOINCREMENT, name TEXT, gender TEXT,
        birth_date TEXT, id_number TEXT UNIQUE, school_name TEXT, school_code TEXT,
        major TEXT, education_level TEXT, education_mode TEXT, degree TEXT,
        enrollment_date TEXT, graduation_date TEXT, certificate_number TEXT UNIQUE,
        certificate_type TEXT, status TEXT DEFAULT '毕业')""")
    c.execute("""CREATE TABLE verify_records (
        id INTEGER PRIMARY KEY AUTOINCREMENT, file_name TEXT, ocr_full_text TEXT,
        ocr_confidence REAL, extracted_fields TEXT, verify_result TEXT,
        alert_level TEXT, details TEXT, created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP)""")
    c.execute("""CREATE TABLE IF NOT EXISTS blacklist (
        id_number TEXT PRIMARY KEY, name TEXT, reason TEXT, added_date TEXT)""")

    # 导入院校
    for code, name, province, level in SCHOOLS:
        c.execute("INSERT OR IGNORE INTO schools VALUES (?,?,?,?)", (code, name, province, level))
    print(f"Schools: {len(SCHOOLS)}")

    # 检查广东理工学院是否在列表中
    c.execute("SELECT * FROM schools WHERE school_name = '广东理工学院'")
    row = c.fetchone()
    print(f"广东理工学院: {'YES' if row else 'NOT FOUND'}")

    # ─── 生成学籍数据：每个学校保证至少2条 ───
    count = 0
    used_ids = set()
    used_certs = set()
    schools_list = SCHOOLS.copy()

    def gen_record(school):
        nonlocal count
        code, name, province, level = school

        grad_year = random.randint(2020, 2024)
        enroll_year = grad_year - random.choice([3, 4])
        birth_year = enroll_year - random.randint(17, 21)

        area = random.choice(["110101","310101","440103","320102","330102","420102",
                              "510104","610103","210102","370102","350102","410102"])
        birth = f"{birth_year}{random.randint(1,12):02d}{random.randint(1,28):02d}"
        seq = random.randint(100, 999)
        prefix = f"{area}{birth}{seq}"
        weights = [7,9,10,5,8,4,2,1,6,3,7,9,10,5,8,4,2]
        total = sum(int(prefix[i]) * weights[i] for i in range(17))
        id_no = prefix + "10X98765432"[total % 11]

        cert_no = f"{code}{grad_year}01{random.randint(10000,99999):05d}"

        if id_no in used_ids or cert_no in used_certs:
            return None
        used_ids.add(id_no)
        used_certs.add(cert_no)

        edu = "本科" if level == "本科" else "专科"
        degree = "学士" if level == "本科" else None
        edu_mode = random.choices(["全日制","全日制","全日制","全日制","非全日制"], weights=[85,85,85,85,10])[0]

        return (
            fake.name(), random.choice(["男","女"]), f"{birth_year}-{birth[4:6]}-{birth[6:8]}",
            id_no, name, code,
            random.choice(MAJORS), edu, edu_mode, degree,
            f"{enroll_year}-09-01", f"{grad_year}-07-01", cert_no,
            "毕业证书", "毕业"
        )

    # 第一轮：每个学校至少2条
    for school in schools_list:
        for _ in range(3):  # 尝试3次
            rec = gen_record(school)
            if rec:
                c.execute("""INSERT OR IGNORE INTO mock_chsi
                    (name,gender,birth_date,id_number,school_name,school_code,
                     major,education_level,education_mode,degree,
                     enrollment_date,graduation_date,certificate_number,certificate_type,status)
                    VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)""", rec)
                count += 1

    print(f"After guaranteed coverage: {count} records")

    # 第二轮：随机补充到3000条
    while count < 3000:
        school = random.choice(schools_list)
        rec = gen_record(school)
        if rec:
            c.execute("""INSERT OR IGNORE INTO mock_chsi
                (name,gender,birth_date,id_number,school_name,school_code,
                 major,education_level,education_mode,degree,
                 enrollment_date,graduation_date,certificate_number,certificate_type,status)
                VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)""", rec)
            count += 1

    # 验证覆盖率
    c.execute("SELECT COUNT(DISTINCT school_code) FROM mock_chsi")
    covered = c.fetchone()[0]
    print(f"Chsi records: {count}, Schools covered: {covered}/{len(SCHOOLS)}")

    # 黑名单
    for _ in range(15):
        id_no = f"{random.choice(['110101','440103','510104'])}{random.randint(1985,2000):04d}{random.randint(1,12):02d}{random.randint(1,28):02d}{random.randint(100,999):03d}"
        w = [7,9,10,5,8,4,2,1,6,3,7,9,10,5,8,4,2]
        t = sum(int(id_no[i]) * w[i] for i in range(17))
        id_no = id_no[:17] + "10X98765432"[t % 11]
        c.execute("INSERT OR IGNORE INTO blacklist VALUES (?,?,?,?)", (
            id_no, fake.name(),
            random.choice(["学历造假","证书伪造","学历层次虚报","使用他人学历"]),
            f"202{random.randint(2,5)}-0{random.randint(1,9)}-{random.randint(10,28):02d}"
        ))

    conn.commit()
    conn.close()
    print("Done!")


